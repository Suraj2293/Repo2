package net.paladion.model;

import java.sql.Timestamp;

import lombok.Getter;
import lombok.Setter;

/**
 * @author ankush
 *
 */
public class EveryConditionData {
  @Getter
  @Setter
  private long threatId;
  @Getter
  @Setter
  private Timestamp eventTime;
  @Getter
  @Setter
  private long count;

  /*
   * (non-Javadoc)
   * 
   * @see java.lang.Object#toString()
   */
  @Override
  public String toString() {
    return "EveryConditionData [threatId=" + threatId + ", eventTime=" + eventTime + ", count="
        + count + "]";
  }
}
